package ForLoop;

public class ForLoop {
	public static void main(String[] args) {
		// increase i value ++
		for (int i = 0; i <= 5; i++) {
			System.out.println("i=" + i);
		}
		{
			// Empty space between outputs
			System.out.println();
		}
		// decrement j value --
		for (int j = 10; j > 5; j--) {
			// without line comment (print)
			System.out.print("j=" + j);
		}
	}
}
